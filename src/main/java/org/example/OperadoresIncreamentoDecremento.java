package org.example;

public class OperadoresIncreamentoDecremento {

    public static void main(String[] args) {

        int x = 10;
        System.out.println("Valor incial de x " + x);
        x++;
        System.out.println("x++ -> " + x);
        ++x;
        System.out.println("x++ -> " + x);

    }

}
