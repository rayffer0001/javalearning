package org.example;

public class OperadoresRelacionales {

    public static void main(String[] args) {

        int x = 10;
        int y = 20;

        System.out.println("x == y: " + (x == y));
        System.out.println("x != y: " + (x != y));
        System.out.println("x == y: " + (x < y));
        System.out.println("x == y: " + (x > y));
        System.out.println("x == y: " + (x >= y));
        System.out.println("x == y: " + (x <= y));


    }
}
