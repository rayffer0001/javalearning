package org.example;

public class OperadoresAsignacion {

    public static void main(String[] args) {

        int x = 10;
        System.out.println("Valor incial de x " + x);
        x += 5;
        System.out.println("x += 5: " + x);
        x -= 5;
        System.out.println("x -= 5: " + x);
        x *= 5;
        System.out.println("x *= 5: " + x);
        x /= 5;
        System.out.println("x /= 5: " + x);

    }

}
